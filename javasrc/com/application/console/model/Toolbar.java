package com.application.console.model;
/**
 * 工具条属性实体类
 * @author wangyf
 * @version 1.0
 */
public class Toolbar {
	private String key;
	private String code;
	private String name;
	private String freshTree;
	private String freshOpener;
	private String closeWindow;
	private String loadCondition;
	private String executeType;
	private String href;
	private String onclick;
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}	
	public String getFreshTree() {
		return freshTree;
	}
	public void setFreshTree(String freshTree) {
		this.freshTree = freshTree;
	}
	public String getFreshOpener() {
		return freshOpener;
	}
	public void setFreshOpener(String freshOpener) {
		this.freshOpener = freshOpener;
	}
	public String getCloseWindow() {
		return closeWindow;
	}
	public void setCloseWindow(String closeWindow) {
		this.closeWindow = closeWindow;
	}
	public String getLoadCondition() {
		return loadCondition;
	}
	public void setLoadCondition(String loadCondition) {
		this.loadCondition = loadCondition;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	public String getOnclick() {
		return onclick;
	}
	public void setOnclick(String onclick) {
		this.onclick = onclick;
	}
	public String getExecuteType() {
		return executeType;
	}
	public void setExecuteType(String executeType) {
		this.executeType = executeType;
	}
	
}
